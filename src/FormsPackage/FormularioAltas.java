    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

                
        try{
            Connection cn = db.connector.getConnection();
            String sql = "insert into inventariotable(codigo,articulo,sucursal,existencia,marca) values(?,?,?,?,?)";
            java.sql.PreparedStatement ps = cn.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(jTextField1.getText()));
            ps.setString(2, jTextField2.getText());
            ps.setString(3, (String)jComboBox1.getSelectedItem());
            ps.setInt(4, Integer.parseInt(jTextField4.getText()));
            ps.setString(5, jTextField3.getText());
            ps.execute();
            JOptionPane.showMessageDialog(this, "Guardado exitosamente");

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this,e);
        }        
        
    }//GEN-LAST:event_sendButtonActionPerformed

